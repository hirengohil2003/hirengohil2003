#String And It's Methods.

#upper() Method..
s1="Hiren Gohil"
x=s1.upper()
print(x)

#lower() Method..
s2="Hello"
y=s2.lower()
print(y)

#join() Method..
z=['Jay','Hiren','Darshil']
res=s2.join(z)
print(res)

#split() Method..
s3="Array Pointer Structure Union "
splited=s3.split()
print(splited)  #Splited String In Form Of List.

#find() Method..
s4="Hello ,My Name Is Hiren Gohil."
find=s4.find("Name")  
print(find)   #It Will Print Index Of That String That You Want To Search.

#replace() Method..
replace=s4.replace("Hello","Good Morning")
print(replace)

